# メイン処理
try {
    # 前提条件チェック
    Test-Prerequisites

    # ディレクトリ作成
    Write-Host "📁 ディレクトリ '$NewDirName' を作成中..." -ForegroundColor Yellow
    New-Item -Path $NewDirPath -ItemType Directory -Force | Out-Null
    Write-Host "   ✅ ディレクトリを作成しました" -ForegroundColor Green

    # テンプレートファイルのコピー
    Write-Host "📄 テンプレートファイルをコピー中..." -ForegroundColor Yellow
    $copiedFiles = @()

    if ($Language -eq "cpp" -or $Language -eq "both") {
        $cppSource = Join-Path $TemplateDir "main.cpp"
        $cppDest = Join-Path $NewDirPath "main.cpp"
        Copy-Item -Path $cppSource -Destination $cppDest
        $copiedFiles += "main.cpp"
        Write-Host "   ✅ main.cpp をコピーしました" -ForegroundColor Green
    }

    if ($Language -eq "python" -or $Language -eq "both") {
        $pySource = Join-Path $TemplateDir "main.py"
        $pyDest = Join-Path $NewDirPath "main.py"
        Copy-Item -Path $pySource -Destination $pyDest
        $copiedFiles += "main.py"
        Write-Host "   ✅ main.py をコピーしました" -ForegroundColor Green
    }

    # README.md生成
    Write-Host "📝 README.md を生成中..." -ForegroundColor Yellow
    $readmeContent = New-ReadmeContent -Number $ContestNumber -ContestDate $Date
    $readmePath = Join-Path $NewDirPath "README.md"
    $readmeContent | Out-File -FilePath $readmePath -Encoding UTF8
    Write-Host "   ✅ README.md を生成しました" -ForegroundColor Green

    # 成功メッセージ
    Write-Host "`n" + ("=" * 50) -ForegroundColor Gray
    Write-Host "🎉 ABC$ContestNumber のフォルダ作成が完了しました！" -ForegroundColor Green
    Write-Host "� 作成場所: $NewDirPath" -ForegroundColor Cyan
    Write-Host "📋 作成されたファイル:" -ForegroundColor Cyan
    foreach ($file in ($copiedFiles + "README.md")) {
        Write-Host "   - $file" -ForegroundColor White
    }

    # フォルダを開く
    if ($Open) {
        Write-Host "📁 フォルダを開いています..." -ForegroundColor Yellow
        Start-Process explorer.exe -ArgumentList $NewDirPath
    }

    Write-Host "`n🚀 コンテスト頑張ってください！" -ForegroundColor Magenta

} catch {
    Write-Host "`n❌ エラーが発生しました: $($_.Exception.Message)" -ForegroundColor Red
    exit 1
}