#Requires -Version 5.1
<#
.SYNOPSIS
    AtCoder ABC コンテスト用フォルダを自動生成するスクリプト

.DESCRIPTION
    指定された回数のAtCoder Beginner Contest (ABC) 用のフォルダを作成し、
    テンプレートファイルをコピーして、コンテスト情報付きのREADME.mdを生成します。

.PARAMETER ContestNumber
    作成するコンテストの回数（必須）

.PARAMETER Force
    既存のフォルダがある場合に上書きします

.PARAMETER Open
    作成後にフォルダを開きます

.PARAMETER Language
    使用言語を指定します（cpp, python, both）。デフォルトはboth

.PARAMETER Date
    コンテスト開催日を指定します（YYYY-MM-DD形式）

.EXAMPLE
    .\create_abc.ps1 372
    ABC372フォルダを作成

.EXAMPLE
    .\create_abc.ps1 372 -Force -Open -Date "2025-01-01"
    ABC372フォルダを強制作成し、開催日を設定してフォルダを開く

.EXAMPLE
    .\create_abc.ps1 372 -Language cpp
    C++テンプレートのみでABC372フォルダを作成
#>

[CmdletBinding()]
param(
    [Parameter(Mandatory=$true, Position=0)]
    [ValidateRange(1, 999)]
    [int]$ContestNumber,

    [Parameter()]
    [switch]$Force,

    [Parameter()]
    [switch]$Open,

    [Parameter()]
    [ValidateSet("cpp", "python", "both")]
    [string]$Language = "both",

    [Parameter()]
    [ValidatePattern('^\d{4}-\d{2}-\d{2}$')]
    [string]$Date,

    [Parameter()]
    [switch]$VSCode,

    [Parameter()]
    [switch]$CreateTestFiles
)

# エラー処理の設定
$ErrorActionPreference = 'Stop'

# スクリプト情報
$ScriptVersion = "2.0.0"
$ScriptDir = Split-Path -Parent $MyInvocation.MyCommand.Definition
$TemplateDir = Join-Path $ScriptDir "template"
$NewDirName = "ABC$ContestNumber"
$NewDirPath = Join-Path $ScriptDir $NewDirName

Write-Host "🚀 AtCoder ABC フォルダ生成ツール v$ScriptVersion" -ForegroundColor Cyan
Write-Host "=" * 50 -ForegroundColor Gray

# バリデーション
function Test-Prerequisites {
    # テンプレートディレクトリの存在確認
    if (!(Test-Path $TemplateDir)) {
        throw "テンプレートディレクトリ '$TemplateDir' が見つかりません。"
    }

    # 必要なテンプレートファイルの確認
    $requiredFiles = @()
    if ($Language -eq "cpp" -or $Language -eq "both") {
        $requiredFiles += "main.cpp"
    }
    if ($Language -eq "python" -or $Language -eq "both") {
        $requiredFiles += "main.py"
    }

    foreach ($file in $requiredFiles) {
        $filePath = Join-Path $TemplateDir $file
        if (!(Test-Path $filePath)) {
            throw "必要なテンプレートファイル '$file' が見つかりません。"
        }
    }

    # 既存ディレクトリのチェック
    if (Test-Path $NewDirPath) {
        if ($Force) {
            Write-Warning "既存のディレクトリ '$NewDirName' を削除します..."
            Remove-Item -Path $NewDirPath -Recurse -Force
        } else {
            throw "ディレクトリ '$NewDirName' は既に存在します。-Force オプションを使用して上書きしてください。"
        }
    }

